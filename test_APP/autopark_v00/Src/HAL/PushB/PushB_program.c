/*******************************************************************************************************/
/*                                      01- PushB_Init                               		           */
/*-----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Set The Mode Of Pin Input Take PushB_Read structure      	   */
/*                                                                                                     */
/* 2- Function Input       ->  PushBX_t *PushB 								                           */
/* 3- Function Return      -> No Thing                                                                 */
/*******************************************************************************************************/
void PushB_Init(const PushBX_t *PushB){

	MGPIO_voidSetPinMode(PushB->PushB_port,PushB->PushB_Pin,MODE_INPUT);
	MGPIO_voidSetPullType(PushB->PushB_port,PushB->PushB_Pin,PULL_DOWN);

}


/*******************************************************************************************************/
/*                                      02- PushB_DeInit                               			       */
/*-----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function remove pull from the pin #####(need to lock this pin also)######*/
/*                                                                                                     */
/* 2- Function Input       ->  PushBX_t *PushB 								                           */
/* 3- Function Return      -> No Thing                                                                 */
/*******************************************************************************************************/
void PushB_DeInit(const PushBX_t *PushB){

	MGPIO_voidSetPullType(PushB->PushB_port,PushB->PushB_Pin,PULL_OFF);

}


/*******************************************************************************************************/
/*                                      03- PushB_Read                               		     	   */
/*-----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function read the button status       							 	   */
/*                                                                                                     */
/* 2- Function Input       -> LedX_t *LED 									                           */
/* 3- Function Return      -> the button status                                                        */
/*******************************************************************************************************/
u8 PushB_Read(const PushBX_t *PushB){

	u8 L_u8Status = 0 ;
	L_u8Status=MGPIO_u8ReadData(PushB->PushB_port,PushB->PushB_Pin);
	return L_u8Status;
}



